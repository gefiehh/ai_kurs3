def get_help(bot_name: str = None) -> str:
    """Возвращает информацию о возможностях бота"""
    name_part = f"{bot_name}, " if bot_name else ""
    return (f"{name_part}я могу:\n"
            "• Узнать погоду в любом городе\n"
            "• Сказать текущее время и дату\n"
            "• Сложить два числа\n"
            "• Запомнить твоё имя\n"
            "• Поддержать разговор\n"
            "• Ответить на «как дела», «привет» и т.д.")